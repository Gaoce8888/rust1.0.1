const EmojiPicker = ({ onSelect, className = '' }) => {
  const [activeTab, setActiveTab] = useState('people');

  const handleEmojiClick = (emoji) => {
    onSelect?.(emoji);
  };

  const tabItems = useMemo(() => {
    return Object.entries(emojiData).map(([key, data]) => ({
      key,
      label: data.label,
      children: (
        <div className="grid grid-cols-8 gap-1 p-2">
          {data.emojis.map((emoji, index) => (
            <button
              key={index}
              className="w-8 h-8 flex items-center justify-center text-xl hover:bg-gray-100 rounded transition-colors"
              onClick={() => handleEmojiClick(emoji)}
            >
              {emoji}
            </button>
          ))}
        </div>
      )
    }));
  }, []);

  return (
    <div className={`emoji-picker bg-white rounded-lg shadow-lg ${className}`} style={{ width: 320 }}>
      <Tabs
        activeKey={activeTab}
        onChange={setActiveTab}
        items={tabItems}
        size="small"
        className="emoji-tabs"
      />
    </div>
  );
};

